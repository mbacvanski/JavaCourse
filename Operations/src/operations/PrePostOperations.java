package operations;

public class PrePostOperations {

	public static void main(String[] args) {
		int num = 42;
		//Declares & assigns num to 42.
		System.out.println("Num is equal to: " + num);
		System.out.println("Num++ is equal to: " + num++);
		num = 42;
		System.out.println("++Num is equal to: " + ++num);
		num = 42;
		System.out.println("Num-- is equal to: " + num--);
		num = 42;
		System.out.println("--Num is equal to: " + --num);
	}

}
